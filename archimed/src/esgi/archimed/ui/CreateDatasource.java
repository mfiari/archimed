    private void buttonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCreateActionPerformed
        String item = this.comboBoxDatasource.getSelectedItem().toString();
        String name = this.textFieldName.getText();
        Datasource datasource;
        switch (item) {
            case "XML datasource" :
                String url = ((JTextField)this.panelDatasource.getComponent(0)).getText();
                if (name.isEmpty()) {
                    datasource = new XMLDatasource(url);
                } else {
                    datasource = new XMLDatasource(name, url);
                }
                break;
            case "SQL datasource" :
                String host = ((JTextField)this.panelDatasource.getComponent(0)).getText();
                int port = Integer.parseInt(((JTextField)this.panelDatasource.getComponent(1)).getText());
                String login = ((JTextField)this.panelDatasource.getComponent(2)).getText();
                String password = ((JTextField)this.panelDatasource.getComponent(3)).getText();
                String database = ((JTextField)this.panelDatasource.getComponent(4)).getText();
                if (name.isEmpty()) {
                    datasource = new SQLDatasource(host, port, login, password, database);
                } else {
                    datasource = new SQLDatasource(name, host, port, login, password, database);
                }
                break;
            default : 
                datasource = null;
                break;
        }
        Component [] components = this.panelAdapter.getComponents();
        int index = 0;
        boolean isCheckboxesSelected = false;
        for (Component component : components) {
            if (component instanceof JCheckBox) {
                JCheckBox checkBox = (JCheckBox) component;
                if (checkBox.isSelected()) {
                    isCheckboxesSelected = true;
                    Adapter adapter = this.mediateur.getAdapters().get(index);
                    if (!adapter.addDatasource(datasource)) {
                        JOptionPane.showMessageDialog(this, "This datasource is not compitible with this adapter.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
                index++;
            }
        }
        if (!isCheckboxesSelected) {
            JOptionPane.showMessageDialog(this, "No adapter selected.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            dispose();
        }
    }//GEN-LAST:event_buttonCreateActionPerformed

